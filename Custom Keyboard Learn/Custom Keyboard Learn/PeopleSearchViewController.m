- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier];
    }
    // Configure the cell...
    int row = indexPath.row;
    ABRecordRef ref = CFArrayGetValueAtIndex(allPeople, row);
    NSString *firstName = (__bridge_transfer NSString*)ABRecordCopyValue(ref, kABPersonFirstNameProperty);
    NSString *lastName = (__bridge_transfer NSString*)ABRecordCopyValue(ref, kABPersonLastNameProperty);

    if (firstName && lastName) {
         cell.textLabel.text = [firstName stringByAppendingFormat: @" %@", lastName];
        return cell;
    }

    if (firstName && !lastName) {
        cell.textLabel.text = firstName;
        return cell;
    }

    if (!firstName && lastName) {
        cell.textLabel.text = lastName;
        return cell;
    }

    return cell;
}

